void	ft_ultimate_div_mod(int *a, int*b)
{
	int	aux1;
	int	aux2;

	aux1 = *a;
	aux2 = *b;
	if (*b != 0)
	{
		*a = aux1 / aux2;
		*b = aux1 % aux2;
	}
}
