#include "../include/minishell.h"

int	ft_pwd(void)
{
	char	*cwd;

	cwd = getcwd(NULL, 0);
	if (cwd)
	{
		printf("%s\n", cwd);
		return (free(cwd), 0);
	}
	else
	{
		perror("pwd");
		return (free(cwd), 1);
	}
}
